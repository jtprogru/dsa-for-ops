"""Задача 1. Пара соседних элементов с минимальной суммой.

Дан список целых чисел. Найдите пару соседних элементов с минимальной
суммой и верните индекс левого элемента пары и саму сумму.

Пример: для [10, 3, 8, 1, 9, 4] минимальная сумма у пары с индексами
2 и 3: 8 + 1 = 9 -> (2, 9).

Закрепляет: проход по соседним парам через индексы. За один проход держим
текущий минимум, не строя вспомогательных списков.
Сложность: O(n) время, O(1) память.
"""

import random


def min_adjacent_pair(nums: list[int]) -> tuple[int, int]:
    """Возвращает (индекс левого элемента, сумму) пары соседей с минимальной суммой.

    Требует не менее двух элементов, иначе соседней пары не существует.
    """
    if len(nums) < 2:
        raise ValueError("нужно хотя бы два элемента, чтобы образовать пару соседей")
    best_index = 0
    best_sum = nums[0] + nums[1]
    for i in range(1, len(nums) - 1):
        current = nums[i] + nums[i + 1]
        if current < best_sum:
            best_index, best_sum = i, current
    return best_index, best_sum


if __name__ == "__main__":
    arr = [random.randint(1, 100) for _ in range(6)]
    print(arr)
    index, total = min_adjacent_pair(arr)
    print(f"Минимальная сумма у пары с индексами {index} и {index + 1}: {total}")
