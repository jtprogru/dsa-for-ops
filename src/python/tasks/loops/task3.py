"""Задача 3. Подсчёт отрицательных элементов.

В заданном списке из положительных и отрицательных чисел посчитайте
количество отрицательных элементов и верните его.

Закрепляет: накопление счётчика в цикле по условию.
Сложность: O(n) время, O(1) память.
"""


def count_negatives(nums: list[int]) -> int:
    """Возвращает количество отрицательных элементов списка."""
    count = 0
    for num in nums:
        if num < 0:
            count += 1
    return count


if __name__ == "__main__":
    sample = [3, -1, -7, 0, 5, -2]
    print(sample)
    print(f"Отрицательных элементов: {count_negatives(sample)}")
